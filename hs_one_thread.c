// INCLUDES:

#include <stdlib.h>
#include <stdio.h>
#include <pthread.h>
#include <math.h>
#include <string.h>

// GLOBALS:

pthread_mutex_t mutex_lock;  // mutually exclusive lock for threads
int* input_g; // global input array
int* sums_g;  // global prefix sum array
int size_g;  // global size of input;

// FUNCTIONS:

void* threadFunction(void* par)
{
	int step = 0;  // step/row counter
	int jump = 1;  // how many numbers to jump forward to calculate current sum, start at 1 because 2^0 = 1
	int mid_sums[size_g];  // place to temporarily hold sums in between steps
	memcpy(mid_sums, input_g, sizeof(input_g));

	while(jump < size_g)
	{  // while there is another sum to be calculated
		int loops = (size_g - jump);  // how many sums to be calculated this step
		for(int i = 0; i < loops; i++)
		{  // set input values for next step
			mid_sums[jump + i] = (input_g[jump + i] + input_g[i]);
		}
		memcpy(input_g, mid_sums, sizeof(mid_sums));
		for(int i = 0; i < jump; i++)
		{  // set values in prefix sum array
			sums_g[i] = input_g[i];
		}
		step++;	
		jump = pow(2, step);
	}
	for(int i = (jump / 2); i < size_g; i++)
	{
		sums_g[i] = input_g[i];
	}
	for(int i = 0; i < size_g; i++)
	{
		printf("%d\n", sums_g[i]);
	}

	return 0;
}

/*
	input = int array
	size = size of input
	nt = number of threads
*/
void production(int* input, int size, int nt)
{
	pthread_t threads[nt];  // array to store thread

	for(int i = 0; i < 1; i++)
	{  // create (nt) threads
		pthread_create(&threads[i], NULL, threadFunction, 0);
	}
	for(int i = 0; i < 1; i++)
	{  // wait for all threads to finish executing
		pthread_join(threads[i], NULL);
	}

	return;
}

int main()
{
	int input[6] = {1, 2, 3, 4, 5, 6};  // input array
	int size = 6;  // size of input array
	int nt = 2;  // number of threads
	int sums[size];  // array of prevfix sums

	input_g = input;  // make input global
	sums_g = sums;  // make sums global
	size_g = size;  // make size global

	production(input, size, nt);

	return 0;
}
